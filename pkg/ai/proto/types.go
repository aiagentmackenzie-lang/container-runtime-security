// ── Message Types ──────────────────────────────────────────────────────

// SecurityEvent represents a single security event for AI analysis.
// Mirrors the protobuf message SecurityEvent from securityscarlet.proto.
type SecurityEvent struct {
	TimestampNS  uint64
	PID          uint32
	PPID         uint32
	UID          uint32
	GID         uint32
	Category    uint32
	EventType   uint32
	SyscallNr   uint32
	Comm        string
	ContainerID string
	ContainerImage string
	Namespace   string

	// Oneof payload (only one should be set based on Category)
	ProcessPayload  *ProcessPayload
	FilePayload      *FilePayload
	NetworkPayload   *NetworkPayload
	EscapePayload    *EscapePayload
	PrivilegePayload *PrivilegePayload
}

// ProcessPayload holds process-specific event data.
type ProcessPayload struct {
	Filename string
	Args     string
}

// FilePayload holds file-specific event data.
type FilePayload struct {
	Path  string
	Flags uint32
	Mode  uint32
}

// NetworkPayload holds network-specific event data.
type NetworkPayload struct {
	RemoteAddr []byte
	LocalAddr  []byte
	RemotePort uint32
	LocalPort  uint32
	Protocol   uint32
}

// EscapePayload holds container escape event data.
type EscapePayload struct {
	NSType   uint32
	TargetNS []uint32
}

// PrivilegePayload holds privilege escalation event data.
type PrivilegePayload struct {
	OldUID    uint32
	NewUID    uint32
	Capability uint32
	ModeFlags  uint32
}

// AnalysisResult is the result of AI event analysis.
type AnalysisResult struct {
	AnomalyScore      float64
	Classification    string
	Description      string
	EnforceRecommended bool
	Confidence       float64
	ModelVersion     string
}

// ProfileRequest asks the AI service for a behavioral profile.
type ProfileRequest struct {
	Image        string
	ForceRebuild bool
}

// BehavioralProfile contains a per-image behavioral baseline.
type BehavioralProfile struct {
	Image          string
	SyscallProfile *SyscallProfile
	NetworkProfile *NetworkProfile
	FileProfile    *FileProfile
	BaselineEvents uint64
	Confidence     float64
}

// SyscallProfile describes expected syscall patterns.
type SyscallProfile struct {
	TopSyscalls       map[string]float64
	UniqueSyscallCount int
	NgramHashes       []uint64
}

// NetworkProfile describes expected network behavior.
type NetworkProfile struct {
	ExpectedPorts    map[uint32]float64
	ExpectedProtocols map[string]float64
	ConnectionRate   float64
}

// FileProfile describes expected file access patterns.
type FileProfile struct {
	ReadPaths     []string
	WritePaths    []string
	DriftBaseline []string
}

// Alert represents a security alert submitted for triage.
type Alert struct {
	RuleID        string
	Priority      string
	Namespace     string
	Container     string
	Output        string
	ProcessName   string
	PID           uint32
	ContainerImage string
}

// TriageResult is the AI assessment of an alert's validity.
type TriageResult struct {
	FalsePositiveScore float64
	Priority           string
	Reasoning          string
	SuppressRecommended bool
	Confidence         float64
}

// IncidentContext provides context for AI rule suggestion.
type IncidentContext struct {
	RuleID        string
	Events        []IncidentEvent
	ContainerImage string
	Namespace     string
	TimestampNS   uint64
}

// IncidentEvent is a simplified event in the incident context.
type IncidentEvent struct {
	EventType  string
	ProcessName string
	PID        uint32
	Category   string
	TimestampNS uint64
	Details    map[string]string
}

// RuleSuggestion is a draft YAML rule generated by the AI.
type RuleSuggestion struct {
	RuleYAML        string
	Reasoning       string
	BasedOnIncidents int
	Confidence       float64
	Status           string // "draft", "pending_approval", "approved", "rejected"
}

// ── gRPC Client ────────────────────────────────────────────────────────

// SecurityScarletAIClient is the client API for the SecurityScarletAI gRPC service.
// When the server is unavailable, all methods return gracefully degraded results.
type SecurityScarletAIClient struct {
	mu sync.RWMutex

	// gRPC connection
	conn    *grpc.ClientConn
	healthClient grpc_health_v1.HealthClient

	// Configuration
	endpoint string
	timeout  time.Duration

	// State
	connected bool
	healthy   bool
}

// NewSecurityScarletAIClient creates a new gRPC client for the AI service.
func NewSecurityScarletAIClient(endpoint string, timeout time.Duration) *SecurityScarletAIClient {
	if timeout <= 0 {
		timeout = 5 * time.Second
	}
	return &SecurityScarletAIClient{
		endpoint: endpoint,
		timeout:  timeout,
	}
}

// Connect establishes a gRPC connection to the SecurityScarletAI service.
func (c *SecurityScarletAIClient) Connect(ctx context.Context) error {
	c.mu.Lock()
	defer c.mu.Unlock()

	if c.connected {
		return nil
	}

	opts := []grpc.DialOption{
		grpc.WithTransportCredentials(insecure.NewCredentials()),
		grpc.WithBlock(),
	}

	// Apply timeout
	connectCtx, cancel := context.WithTimeout(ctx, c.timeout)
	defer cancel()

	conn, err := grpc.DialContext(connectCtx, c.endpoint, opts...)
	if err != nil {
		return fmt.Errorf("gRPC connection to %s failed: %w", c.endpoint, err)
	}

	c.conn = conn
	c.healthClient = grpc_health_v1.NewHealthClient(conn)
	c.connected = true

	log.Printf("[ai/proto] Connected to SecurityScarletAI at %s", c.endpoint)
	return nil
}

// Disconnect closes the gRPC connection.
func (c *SecurityScarletAIClient) Disconnect() error {
	c.mu.Lock()
	defer c.mu.Unlock()

	if !c.connected || c.conn == nil {
		return nil
	}

	err := c.conn.Close()
	c.connected = false
	c.healthy = false
	c.conn = nil

	log.Printf("[ai/proto] Disconnected from SecurityScarletAI")
	return err
}

// IsConnected returns whether the gRPC connection is active.
func (c *SecurityScarletAIClient) IsConnected() bool {
	c.mu.RLock()
	defer c.mu.RUnlock()
	return c.connected
}

// CheckHealth checks the health of the AI service via gRPC health checking.
func (c *SecurityScarletAIClient) CheckHealth(ctx context.Context) (bool, error) {
	c.mu.RLock()
	hc := c.healthClient
	c.mu.RUnlock()

	if hc == nil {
		return false, fmt.Errorf("not connected")
	}

	healthCtx, cancel := context.WithTimeout(ctx, 3*time.Second)
	defer cancel()

	resp, err := hc.Check(healthCtx, &grpc_health_v1.HealthCheckRequest{
		Service: ServiceName,
	})
	if err != nil {
		c.mu.Lock()
		c.healthy = false
		c.mu.Unlock()
		return false, fmt.Errorf("health check failed: %w", err)
	}

	healthy := resp.GetStatus() == grpc_health_v1.HealthCheckResponse_SERVING
	c.mu.Lock()
	c.healthy = healthy
	c.mu.Unlock()
	return healthy, nil
}

