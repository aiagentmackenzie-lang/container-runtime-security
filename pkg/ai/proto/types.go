// ── Helper: stream wrappers ─────────────────────────────────────────────

// SecurityEventStream wraps a gRPC stream for AnalyzeEvents.
type SecurityEventStream interface {
	Send(event *SecurityEvent) error
	Recv() (*AnalysisResult, error)
	Close() error
}

// eventStream is a bidirectional stream stub for testing.
type eventStream struct {
	events chan *SecurityEvent
	results chan *AnalysisResult
	closed bool
	mu     sync.Mutex
}

// NewEventStream creates a new bidirectional event stream for testing.
func NewEventStream() *eventStream {
	return &eventStream{
		events:  make(chan *SecurityEvent, 100),
		results: make(chan *AnalysisResult, 100),
	}
}

func (s *eventStream) Send(event *SecurityEvent) error {
	s.mu.Lock()
	defer s.mu.Unlock()
	if s.closed {
		return io.EOF
	}
	s.events <- event
	return nil
}

func (s *eventStream) Recv() (*AnalysisResult, error) {
	s.mu.Lock()
	defer s.mu.Unlock()
	if s.closed {
		return nil, io.EOF
	}
	select {
	case result := <-s.results:
		return result, nil
	default:
		return &AnalysisResult{Classification: "unknown"}, nil
	}
}

func (s *eventStream) Close() error {
	s.mu.Lock()
	defer s.mu.Unlock()
	s.closed = true
	return nil
}

// ── Health Server ──────────────────────────────────────────────────────

// RegisterHealthServer registers the gRPC health server on the given
// gRPC server. This enables health checking for the AI service.
func RegisterHealthServer(s *grpc.Server) {
	healthServer := health.NewServer()
	healthServer.SetServingStatus(ServiceName, grpc_health_v1.HealthCheckResponse_SERVING)
	grpc_health_v1.RegisterHealthServer(s, healthServer)
}